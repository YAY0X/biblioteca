    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Importar Libros");
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Archivos PDF", "pdf"));

        int resultado = fileChooser.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();
            String nombreLibro = archivoSeleccionado.getName().replace(".pdf", "");

            try {
                // Leer número de páginas con PDFBox
                int numPaginas = 0;
                try (org.apache.pdfbox.pdmodel.PDDocument documento = org.apache.pdfbox.pdmodel.PDDocument.load(archivoSeleccionado)) {
                    numPaginas = documento.getNumberOfPages();
                }

                // Conectar a la base de datos
                CConexion conexion = new CConexion();
                Connection conn = conexion.estableceConexion();

                if (conn == null) {
                    JOptionPane.showMessageDialog(this, "No se pudo conectar a la base de datos.");
                    return;
                }

                // Obtener lista de códigos Dewey
                String sqlDewey = "SELECT CLAVE, DESCRIPCION FROM codigo_dewey";
                PreparedStatement psDewey = conn.prepareStatement(sqlDewey);
                ResultSet rs = psDewey.executeQuery();

                java.util.List<String> opciones = new java.util.ArrayList<>();
                while (rs.next()) {
                    String clave = rs.getString("CLAVE");
                    String descripcion = rs.getString("DESCRIPCION");
                    opciones.add(clave + " - " + descripcion);
                }
                rs.close();
                psDewey.close();

                if (opciones.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "No hay códigos Dewey disponibles en la base de datos.");
                    conn.close();
                    return;
                }

                // Crear panel personalizado
                JPanel panel = new JPanel(new java.awt.GridLayout(0, 1));
                panel.add(new JLabel("Selecciona el código Dewey:"));
                JComboBox<String> comboDewey = new JComboBox<>(opciones.toArray(new String[0]));
                panel.add(comboDewey);

                panel.add(new JLabel("Autor del libro:"));
                JTextField campoAutor = new JTextField();
                panel.add(campoAutor);

                panel.add(new JLabel("Editorial:"));
                JTextField campoEditorial = new JTextField();
                panel.add(campoEditorial);

                int opcion = JOptionPane.showConfirmDialog(
                    this, 
                    panel, 
                    "Información del libro", 
                    JOptionPane.OK_CANCEL_OPTION, 
                    JOptionPane.PLAIN_MESSAGE
                );

                if (opcion != JOptionPane.OK_OPTION) {
                    JOptionPane.showMessageDialog(this, "Operación cancelada.");
                    conn.close();
                    return;
                }

                String codigoDewey = comboDewey.getSelectedItem().toString().split(" - ")[0];
                String autor = campoAutor.getText().trim();
                String editorial = campoEditorial.getText().trim();

                // Crear carpeta "Libros" si no existe
                File carpetaLibros = new File("Libros");
                if (!carpetaLibros.exists()) {
                    carpetaLibros.mkdirs();
                }

                // Copiar archivo a carpeta del proyecto
                File destino = new File(carpetaLibros, archivoSeleccionado.getName());
                Files.copy(archivoSeleccionado.toPath(), destino.toPath(), StandardCopyOption.REPLACE_EXISTING);

                // Guardar ruta relativa
                String rutaPDF = "Libros/" + archivoSeleccionado.getName();

                // Guardar información completa en la base de datos
                String sqlLibro = "INSERT INTO libro (NOMBRE, ruta_documento, dewey, numpag, autor, editorial) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement psLibro = conn.prepareStatement(sqlLibro);
                psLibro.setString(1, nombreLibro);
                psLibro.setString(2, rutaPDF);
                psLibro.setString(3, codigoDewey);
                psLibro.setInt(4, numPaginas);
                psLibro.setString(5, autor);
                psLibro.setString(6, editorial);
                psLibro.executeUpdate();

                psLibro.close();
                conn.close();

                JOptionPane.showMessageDialog(this,
                    "Libro agregado correctamente:\n\n" +
                    "Título: " + nombreLibro + "\n" +
                    "Autor: " + autor + "\n" +
                    "Editorial: " + editorial + "\n" +
                    "Código Dewey: " + codigoDewey + "\n" +
                    "Número de páginas: " + numPaginas + "\n" +
                    "Guardado en: " + rutaPDF
                );

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al guardar libro: " + e.getMessage());
            }

        } else {
            JOptionPane.showMessageDialog(this, "No se seleccionó ningún archivo.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

